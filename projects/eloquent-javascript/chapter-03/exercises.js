////////////////////////////////////////////////////////////////////////////////
// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(a,b) {
if (a < b) return a;
  else return b;
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(n) {
if (n == 0) return true;
if (n == 1) return false;
if (n < 0) return isEven(-n);
  else return isEven(n - 2);
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, ch) {
var counted = 0;
  for (var i = 0; i < string.length; i++) {
    if (string.charAt(i) == ch) {
      counted += 1;
    }
  }
  return counted;
}



////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////


function countBs(string) {
  return countChars(string, "B");
}


